package com.company.Model;

public class Model {
    private String input;
    private String content;
    private int whenAdded;
    private String tag;
    private boolean value;
    private String header;


    public Model(String input, String content, int whenAdded, String tag, boolean value, String header) {
        this.input = input;
        this.content = content;
        this.whenAdded = whenAdded;
        this.tag = tag;
        this.value = value;
        this.header = header;
    }
}



